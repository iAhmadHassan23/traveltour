        <script>
          document.addEventListener("DOMContentLoaded", function () {
            const texts = ["Swat Kalam", "Arangkel", "Hunza", "Skardu", "Nathia Gali"];
            let index = 0;
            let letterIndex = 0;
            let isDeleting = false;
            const textElement = document.getElementById("dynamic-text1");

            function typeEffect() {
              const currentText = texts[index];

              if (!isDeleting) {
                // Typing effect
                textElement.textContent = currentText.substring(0, letterIndex + 1);
                letterIndex++;

                if (letterIndex === currentText.length) {
                  isDeleting = true;
                  setTimeout(typeEffect, 1500); // Wait before deleting
                } else {
                  setTimeout(typeEffect, 100); // Typing speed
                }
              } else {
                // Deleting effect
                textElement.textContent = currentText.substring(0, letterIndex - 1);
                letterIndex--;

                if (letterIndex === 0) {
                  isDeleting = false;
                  index = (index + 1) % texts.length; // Move to next word
                  setTimeout(typeEffect, 500); // Wait before next word
                } else {
                  setTimeout(typeEffect, 50); // Deleting speed
                }
              }
            }

            // Start typing effect
            typeEffect();
          });
        </script>
